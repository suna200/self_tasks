void array(int arr[4])
{	
	int i,j;

	for(i=0;i<3;i++)
	{
		for(j=0;j<(3-i);j++)
		{
			if(arr[j]>arr[j+1])
			{
				arr[j]=arr[j] * arr[j+1];
				arr[j+1]=arr[j]/arr[j+1];
				arr[j]=arr[j]/arr[j+1];
			}
		}
	}
}

